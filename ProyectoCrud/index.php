	<div id="texto">

		<p>El COLEGIO DE BOYACÁ nace en 1821, en el Congreso de Cúcuta;
			pero es el 17 de mayo de 1822 cuando el Vicepresidente, General
			Francisco de Paula Santander, encargado del Poder Ejecutivo, dicta el
			Decreto de su Fundación.</p>
		</br>
		<p>En 1827, el Colegio es elevado a la categoría de Universidad
			bajo la Rectoría del doctor José Ignacio de Márquez y posteriormente,
			hasta 1885 atravesó por dos épocas más como Universidad, ofreciendo
			entre otros: Estudios de Latinidad, Bellas Artes, Matemáticas,
			Derecho, Filosofía, Medicina, Ingeniería y Agronomía.</p>
		</br>
		<p>
			En 1972, con ocasión de su Sesquicentenario, fue reorganizado como
			Establecimiento Público del Orden Nacional, con Personería</br> Jurídica,
			autonomía administrativa y patrimonio independiente, adscrito al
			Ministerio de Educación Nacional, a través de la Ley Segunda, en el
			Gobierno del doctor Misael Pastrana Borrero, </br> como Presidente de la
			República, y el doctor Luis Carlos Galán como Ministro de Educación.
		</p>
		</br>
		<p>
			Como Instituto Descentralizado del Orden Nacional, cuyo objeto
			fundamental es la Educación Preescolar, Básica Primaria y Secundaria
			y Media cuenta con dos</br> Consejos Directivos el del Establecimiento
			Público y el del Colegio.
		</p>
		</br>
		<p>
			<span class="textosImportantes"><b>MISIÓN:</b></span> Contribuir al
			desarrollo de las potencialidades morales, intelectuales, físicas y
			estéticas de los niños, los jóvenes y adultos, a través de
			experiencias de aprendizaje y la participación activa en el quehacer
			cotidiano, en los campos:</br> familiar, escolar y comunitario del entorno
			local, regional y nacional, para responder a las demandas
			socio-culturales que exige el mundo</br> en permanente cambio.
		</p>
		</br>
		<p>
			<span class="textosImportantes"><b>VISIÓN:</b></span> El Colegio de
			Boyacá es una Institución Educativa de categoría local, regional y
			nacional, con una organización que ejerce liderazgo en el sector, con
			criterios técnico pedagógicos modernos y enmarcados dentro de la
			filosofía del mejoramiento y cambio social.
		</p>

		<p>Se proyecta como formador de juventudes, orientado al
			mejoramiento de la calidad educativa, como esencia del bienestar del
			ser humano y conservación del medio ambiente.</p>

		<p>Se visualiza el bachiller como líder en todas las actividades
			de la vida productiva de la sociedad, en los campos espiritual,
			social, económico, político, artístico y científico.</p>
